#include "3-calc.h"

/**
 * op_add - add arguments
 * @a: value 1
 * @b: value 2
 * Return: return result of function
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract the arg
 * @a: value 1
 * @b: value 2
 * Return: return result of function
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply the arg
 * @a: value 1
 * @b: value 2
 * Return: return result of function
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide the arg
 * @a: value 1
 * @b: value 2
 * Return: return result of function
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Find the remainder
 * @a: value 1
 * @b: value 2
 * Return: return result of function
 */

int op_mod(int a, int b)
{
	return (a % b);
}
